#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	char	*str;
	int		i;

	i = 0;
	str = (char *)malloc(ft_strlen(s) + 1);
	if (!str)
		return (0);
	while (*(s + i))
	{
		*(str + i) = *(s + i);
		i++;
	}
	*(str + i) = 0;
	return (str);
}
